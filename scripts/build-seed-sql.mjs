// Converts public/data/*.json into Supabase-compatible INSERT statements.
// Output goes to supabase/seed.sql, which the user pastes into Supabase SQL Editor.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

function dollarQuote(value) {
  if (value === null || value === undefined) return 'null';
  // Use $g$...$g$ — pick a tag unlikely to appear in our text.
  // If by chance the text contains $g$, switch to a longer tag.
  let tag = 'g';
  while (String(value).includes(`$${tag}$`)) tag += 'g';
  return `$${tag}$${value}$${tag}$`;
}

function num(value) {
  return value === null || value === undefined ? 'null' : String(value);
}

function bool(value) {
  return value === true ? 'true' : 'false';
}

function textArray(arr) {
  if (!arr || arr.length === 0) return 'null';
  const inner = arr.map((s) => `'${String(s).replace(/'/g, "''")}'`).join(',');
  return `ARRAY[${inner}]::text[]`;
}

function jsonb(value) {
  if (value === null || value === undefined) return 'null';
  return `${dollarQuote(JSON.stringify(value))}::jsonb`;
}

function galleryRow(g) {
  return `(${[
    `'${g.id}'`,
    dollarQuote(g.name),
    g.shortName ? dollarQuote(g.shortName) : 'null',
    num(g.lat),
    num(g.lng),
    dollarQuote(g.address),
    `'${g.city ?? 'London'}'`,
    g.borough ? dollarQuote(g.borough) : 'null',
    dollarQuote(g.website),
    g.logoUrl ? dollarQuote(g.logoUrl) : 'null',
    jsonb(g.openingHours ?? null),
    g.description ? dollarQuote(g.description) : 'null',
    textArray(g.tags),
  ].join(', ')})`;
}

function exhibitionRow(e) {
  return `(${[
    `'${e.id}'`,
    dollarQuote(e.title),
    `'${e.galleryId}'`,
    `'${e.startDate}'`,
    `'${e.endDate}'`,
    num(e.priceFrom ?? null),
    num(e.priceTo ?? null),
    e.ticketUrl ? dollarQuote(e.ticketUrl) : 'null',
    `'${e.category}'`,
    textArray(e.tags),
    dollarQuote(e.summary),
    dollarQuote(e.description),
    textArray(e.artists),
    e.curator ? dollarQuote(e.curator) : 'null',
    dollarQuote(e.heroImage),
    dollarQuote(e.heroImageAlt),
    jsonb(e.images ?? null),
    bool(e.featured ?? false),
  ].join(', ')})`;
}

const galleries = JSON.parse(readFileSync(join(ROOT, 'public/data/galleries.json'), 'utf8'));
const exhibitions = JSON.parse(readFileSync(join(ROOT, 'public/data/exhibitions.json'), 'utf8'));

const galleriesSql = `-- ${galleries.length} galleries\ninsert into public.galleries (\n  id, name, short_name, lat, lng, address, city, borough, website, logo_url, opening_hours, description, tags\n) values\n${galleries.map(galleryRow).join(',\n')}\non conflict (id) do nothing;`;

const exhibitionsSql = `-- ${exhibitions.length} exhibitions\ninsert into public.exhibitions (\n  id, title, gallery_id, start_date, end_date, price_from, price_to, ticket_url,\n  category, tags, summary, description, artists, curator, hero_image, hero_image_alt, images, featured\n) values\n${exhibitions.map(exhibitionRow).join(',\n')}\non conflict (id) do nothing;`;

const out = `-- Auto-generated by scripts/build-seed-sql.mjs from public/data/*.json
-- Idempotent: re-running adds only missing rows.
-- Paste into Supabase SQL Editor and Run.

${galleriesSql}

${exhibitionsSql}
`;

mkdirSync(join(ROOT, 'supabase'), { recursive: true });
writeFileSync(join(ROOT, 'supabase', 'seed.sql'), out, 'utf8');
console.log(`[build-seed-sql] wrote supabase/seed.sql`);
console.log(`  galleries:   ${galleries.length}`);
console.log(`  exhibitions: ${exhibitions.length}`);
